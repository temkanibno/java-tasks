package task1;

public class MathOperations {
    public int sumNumbers(int a, int b){
        return a+b;
    }
    public int minusNumbers(int a, int b){
        return a-b;
    }
    public int multipleNumbers(int a, int b){
        return a*b;
    }
    public double divideNumbers(int a, int b){
        return a/b;
    }
}
